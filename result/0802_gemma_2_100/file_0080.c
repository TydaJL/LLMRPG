
#include <stdio.h>

int a() {
    return 10;
}

int b() {
    return 20;
}

int c() {
    return 30;
}

int d() {
    return 40;
}

int main() {
    printf("a() returns: %d\n", a());
    printf("b() returns: %d\n", b());
    printf("c() returns: %d\n", c());
    printf("d() returns: %d\n", d());
    return 0;
}
